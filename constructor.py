class addition:
     first=0
     second=0
     answer=0
def __init__(self,f,s):
    self.first=f
    self.second=s
def display(self):
        print("first number= "+str(self.first))
        print("second number= "+str(self.second))
        print("addition of two number= "+str(self.answer))
def calculate(self):
        self.answer=self.first+self.second
obj=addition(1000,2000)
obj.calculate()
obj.display()
            
    